-- ─── 4. machine_comments ─────────────────────────────────────
create table if not exists public.machine_comments (
  id         bigint    generated always as identity primary key,
  user_id    uuid      not null references auth.users(id) on delete cascade,
  machine_id bigint    not null references public.machines(id) on delete cascade,
  body       text      not null check (char_length(body) between 1 and 500),
  created_at timestamptz not null default now()
);

-- Index for fetching comments by machine quickly
create index if not exists machine_comments_machine_id_idx
  on public.machine_comments(machine_id, created_at desc);

-- RLS
alter table public.machine_comments enable row level security;

-- Any authenticated member can read comments
create policy "members can read comments"
  on public.machine_comments for select
  to authenticated
  using (true);

-- Members can insert their own comments
create policy "members can post comments"
  on public.machine_comments for insert
  to authenticated
  with check (user_id = auth.uid());

-- Users can delete only their own comments; mods/admins can delete any
