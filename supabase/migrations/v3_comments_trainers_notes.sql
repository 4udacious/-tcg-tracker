  on public.machine_comments for delete
  to authenticated
  using (
    user_id = auth.uid()
    or exists (
      select 1 from public.profiles
      where id = auth.uid() and role in ('mod', 'admin')
    )
  );


-- ─── 5. v_machine_comments view (14-day rolling window) ──────
drop view if exists public.v_machine_comments;
create or replace view public.v_machine_comments
  with (security_invoker = true)
as
select
  mc.id,
  mc.user_id,
  mc.machine_id,
  mc.body,
  mc.created_at,
  p.username,
  p.display_name,
  ti.file as trainer_icon_file
from public.machine_comments mc
join public.profiles p on p.id = mc.user_id
left join public.trainer_icons ti on ti.id = p.trainer_icon_id
where mc.created_at >= now() - interval '14 days';


-- ─── 6. RLS on trainer_icons (read-only for everyone) ────────
alter table public.trainer_icons enable row level security;

create policy "anyone can read trainer icons"
  on public.trainer_icons for select
  using (true);


